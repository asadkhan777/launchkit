#!/usr/bin/env node

/**
 * Automatic Component Index Generator for LaunchKit AI UI
 * Scans component directory and generates index.ts exports automatically
 */

import { readdir, writeFile, stat } from 'fs/promises';
import { join, relative, basename, extname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = fileURLToPath(new URL('.', import.meta.url));

/**
 * Configuration for component discovery
 */
const CONFIG = {
  componentDir: join(__dirname, '../packages/ui/src'),
  indexFile: join(__dirname, '../packages/ui/src/index.ts'),
  patterns: {
    components: /^[A-Z][a-zA-Z0-9]*\.(tsx?)$/,
    stories: /\.stories\.(tsx?)$/,
    tests: /\.(test|spec)\.(tsx?)$/,
  },
  excludeDirs: ['stories', '__tests__', 'test', 'tests'],
};

/**
 * Scan directory for component files
 */
async function scanComponents(dir, basePath = '') {
  const components = [];
  const items = await readdir(dir);

  for (const item of items) {
    const fullPath = join(dir, item);
    const stats = await stat(fullPath);
    const relativePath = join(basePath, item);

    if (stats.isDirectory()) {
      // Skip excluded directories
      if (CONFIG.excludeDirs.includes(item)) {
        continue;
      }

      // Recursively scan subdirectories
      const subComponents = await scanComponents(fullPath, relativePath);
      components.push(...subComponents);
    } else if (stats.isFile()) {
      // Check if it's a component file
      if (CONFIG.patterns.components.test(item) && 
          !CONFIG.patterns.stories.test(item) && 
          !CONFIG.patterns.tests.test(item)) {
        
        const name = basename(item, extname(item));
        const importPath = relativePath.replace(/\\/g, '/').replace(/\.tsx?$/, '');
        
        components.push({
          name,
          path: importPath,
          fullPath: relativePath,
        });
      }
    }
  }

  return components;
}

/**
 * Generate TypeScript index content
 */
function generateIndexContent(components) {
  const header = `/**
 * Auto-generated component exports for LaunchKit AI UI
 * Generated on: ${new Date().toISOString()}
 * 
 * DO NOT EDIT MANUALLY - This file is automatically generated
 * Run 'pnpm generate:exports' to regenerate
 */

`;

  // Group components by directory for better organization
  const grouped = components.reduce((acc, comp) => {
    const dir = comp.path.includes('/') ? comp.path.split('/')[0] : 'root';
    if (!acc[dir]) acc[dir] = [];
    acc[dir].push(comp);
    return acc;
  }, {});

  let content = header;

  // Generate exports grouped by directory
  Object.entries(grouped).forEach(([dir, comps]) => {
    if (dir !== 'root') {
      content += `// ${dir.charAt(0).toUpperCase() + dir.slice(1)} Components\n`;
    }

    comps.forEach(comp => {
      content += `export { default as ${comp.name} } from './${comp.path}';\n`;
      
      // Also export any named exports (like variants)
      if (comp.name.toLowerCase().includes('button')) {
        content += `export { buttonVariants } from './${comp.path}';\n`;
      }
    });

    content += '\n';
  });

  // Add utility exports
  content += `// Utilities\n`;
  content += `export { cn } from './lib/utils';\n`;
  content += `export type * from './types';\n`;

  return content;
}

/**
 * Generate Storybook auto-discovery config
 */
function generateStorybookConfig(components) {
  const stories = components.map(comp => {
    const storyPath = comp.path.replace(/\/[^/]+$/, `/${comp.name}.stories.tsx`);
    return {
      title: `Components/${comp.name}`,
      path: storyPath,
    };
  });

  return {
    stories: stories.map(s => s.path),
    metadata: {
      generated: new Date().toISOString(),
      componentCount: components.length,
    },
  };
}

/**
 * Main generation function
 */
async function main() {
  console.log('🔍 Scanning for components...');
  
  try {
    const components = await scanComponents(CONFIG.componentDir);
    
    console.log(`📦 Found ${components.length} components:`);
    components.forEach(comp => {
      console.log(`   ${comp.name} (${comp.path})`);
    });

    // Generate index.ts
    const indexContent = generateIndexContent(components);
    await writeFile(CONFIG.indexFile, indexContent, 'utf-8');
    console.log(`✅ Generated ${CONFIG.indexFile}`);

    // Generate Storybook config
    const storybookConfig = generateStorybookConfig(components);
    const storybookConfigPath = join(__dirname, '../packages/ui/.storybook/auto-stories.json');
    await writeFile(storybookConfigPath, JSON.stringify(storybookConfig, null, 2), 'utf-8');
    console.log(`✅ Generated ${storybookConfigPath}`);

    console.log('\n🎉 Component exports generated successfully!');
    console.log('💡 Tip: Add this to your build process to keep exports synchronized');

  } catch (error) {
    console.error('❌ Error generating component exports:', error);
    process.exit(1);
  }
}

// Run if called directly
if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}
